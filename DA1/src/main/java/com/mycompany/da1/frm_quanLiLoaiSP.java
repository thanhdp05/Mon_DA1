    public void init(){
        fillTable();
    }
    
    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_bangLoaiSP.getModel();
        model.setRowCount(0);
        try {
            List<loaiSP> list = dao.selectAll();
            for (loaiSP lsp : list) {
                Object[] row = {lsp.getMaLoai(), lsp.getTenLoai()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "lỗi truy vấn dữ liệu");
        }
    }

    public void setForm(loaiSP lsp) {   
        lbl_maLoai.setText(String.valueOf(lsp.getMaLoai()));
        txt_tenLoai.setText(lsp.getTenLoai());
    }

    loaiSP getForm() {
        loaiSP lsp = new loaiSP();                  
        
        lsp.setTenLoai(txt_tenLoai.getText());        
        if(this.row > 0){
            lsp.setMaLoai(Integer.parseInt(lbl_maLoai.getText()));
        }
        return lsp;
    }
    
    public void clearForm() {
        loaiSP lsp = new loaiSP();
        this.setForm(lsp);
        this.row = -1;
        this.updateStatus();
    }
    
    public void them() {
        loaiSP lsp = getForm();
        try {
            dao.insert(lsp);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm thành công");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm không thành công");
        }
    }
    public void sua() {
        loaiSP lsp = getForm();
        try {
            dao.update(lsp);
            this.fillTable();
            
            MsgBox.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Cập nhật không thành công");
        }
    }
    
    public void xoa() {
        if (MsgBox.confirm(this, "Bạn có muốn xóa hay không?")) {
            int maLoai =Integer.parseInt(lbl_maLoai.getText()) ;
            try {
                dao.delete(maLoai);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                e.printStackTrace();
                MsgBox.alert(this, "Xóa thất bại!");
            }
        }
    }
    
     public void edit() {
        int sp = (Integer) tbl_bangLoaiSP.getValueAt(this.row, 0);
        loaiSP lsp = dao.selectById(sp);
        this.setForm(lsp);
        tabs.setSelectedIndex(1);
        this.updateStatus();
    }
     
     
        public void dauTien() {
        this.row = 0;
        this.edit();
    }
    
    public void troVe() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }
    
    public void tiepTheo() {
        if (this.row < tbl_bangLoaiSP.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }
    
    public void cuoiCung() {
        this.row = tbl_bangLoaiSP.getRowCount() - 1;
        this.edit();
    }
    
      public void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tbl_bangLoaiSP.getRowCount() - 1);
        
        btn_Them.setEnabled(!edit);
        btn_Sua.setEnabled(edit);
        btn_Xoa.setEnabled(edit);
        
        btn_dauTien.setEnabled(edit && !first);
        btn_troVe.setEnabled(edit && !first);
        btn_tiepTheo.setEnabled(edit && !last);
        btn_cuoiCung.setEnabled(edit && !last);
    }
     
