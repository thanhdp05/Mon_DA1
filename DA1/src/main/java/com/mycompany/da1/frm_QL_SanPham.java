    sanPham getForm() {
        if (this.batLoiKoNullSp()) {
            return null;
        }
        sanPham sp = new sanPham();
        sp.setMaSP(txtMaSP.getText());
        sp.setTenSP(txtTenSP.getText());
        int maLoai = ((loaiSP) cboLoaiSP.getSelectedItem()).getMaLoai();
        sp.setLoaiSP(maLoai);
        sp.setHangSP(txtHang.getText());
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        sp.setGiaTien(Float.parseFloat(txtGiaTien.getText()));
        sp.setSize(txtSize.getText());
        sp.setGhiChu(txtGhiChu.getText());
        sp.setMaNV(Auth.user.getMaNV());
        sp.setHinh(lblHinhSP.getToolTipText());
        sp.setNgayTao(new Date());

        return sp;
    }

    public boolean batLoiKoNullSp() {
        if (txtMaSP.getText().isEmpty() || txtTenSP.getText().isEmpty() || txtHang.getText().isEmpty() || txtSoLuong.getText().isEmpty() || txtGiaTien.getText().isEmpty()
                || txtSize.getText().isEmpty()) {
            MsgBox.alert(this, "Không được bỏ trống");
            return true;
        } else {
            return false;
        }

    }

    public boolean batLoiNullLoaiSp() {
        if (txtTenLoai.getText().isEmpty()) {
            MsgBox.alert(this, "Không được bỏ trống");
            return true;
        } else {
            return false;
        }
    }

    void setForm(sanPham sp) {
        if (row1 < 0) {
            cboLoaiSP.setSelectedIndex(sp.getLoaiSP());
        } else {
            cboLoaiSP.setSelectedIndex(sp.getLoaiSP() - 1);
        }

        txtMaSP.setText(sp.getMaSP());
        txtTenSP.setText(sp.getTenSP());
        txtHang.setText(sp.getHangSP());
        txtSoLuong.setText(sp.getSoLuong() + "");
        txtGiaTien.setText(sp.getGiaTien() + "");
        txtSize.setText(sp.getSize());
        txtGhiChu.setText(sp.getGhiChu());
        lblMaNV.setText(Auth.user.getMaNV());

        if (sp.getNgayTao() == null) {
            SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");
            String text = formater.format(new Date());
            lblNgayTao.setText(text);
        } else {
            lblNgayTao.setText(XDate.toString(sp.getNgayTao(), "yyyy-MM-dd"));
        }

        if (sp.getHinh() != null) {
            ImageIcon icon = XImage.read(sp.getHinh());
            Image image = icon.getImage();
            Image resizeImage = image.getScaledInstance(120, 161, Image.SCALE_DEFAULT);
            icon.setImage(resizeImage);
            lblHinhSP.setIcon(icon);
            lblHinhSP.setToolTipText(sp.getHinh());
        }
    }

    public String getTenLoaiSp(int ma) {
        List<loaiSP> tenlsp = dao_lsp.selectAll();
        for (loaiSP lspNeP : tenlsp) {
            if (lspNeP.getMaLoai() == ma) {
                return lspNeP.getTenLoai();
            }
        }
        return "";
    }

    void fillTable_sp() {
        DefaultTableModel model = (DefaultTableModel) tblBang_SP.getModel();
        model.setRowCount(0);
        try {
            String timTen = txtTimKiem.getText();
            List<sanPham> list_sp = dao_sp.selectByKeyWord(timTen);
            for (sanPham e : list_sp) {
                String ten = getTenLoaiSp(e.getLoaiSP());
                Object[] row = {e.getMaSP(), e.getTenSP(), ten, e.getSoLuong(), e.getSize(), e.getGiaTien()};
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Lỗi try vấn dữ liệu!");
        }
    }

