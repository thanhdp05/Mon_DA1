    void fillComboBoxLoaiSP() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiSP.getModel();
        model.removeAllElements();
        List<loaiSP> list_lsp = dao_lsp.selectAll();
        for (loaiSP lsp : list_lsp) {
            model.addElement(lsp);
        }
    }

    void chonAnh() {
        // nếu người dùng chọn tệp và nhấn open 
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            //lấy tệp đc chọn
            File file = fileChooser.getSelectedFile();
            //lưu tệp đc chọn
            XImage.save(file);
            //đọc ảnh từ tệp
            ImageIcon icon = XImage.read(file.getName());
            //lấy đối tượng image từ imageicon
            Image image = icon.getImage();
            //thay đổi kích thước ảnh thành kích thước của lbl
            Image resizeImage = image.getScaledInstance(120, 161, Image.SCALE_DEFAULT);
            //cập nhật ảnh với ảnh đã thay đổi kích thước
            icon.setImage(resizeImage);
            //hiển thị imageicon trên lbl
            lblHinhSP.setIcon(icon);
            //đặt tooltip cho lbl để hiển thị tên của tệp ảnh
            lblHinhSP.setToolTipText(file.getName());
        }
    }

